# The numpy.floor is a mathematical function that returns the floor of the elements of array. The floor of the scalar x is the largest integer i, such that i <= x.
import numpy as np

d=[.5,1.5,10.1,3.4,5.6,2.3]  ## Floor will return the smallest number 
print("Floor :\n", np.floor(d))

d1 = [133.000, 344.54, 437.56, 44.9, 1.2]
print("Floor :\n", np.floor(d1))
