# The numpy.ceil() is a mathematical function that returns the ceil of the elements of array. 
# The ceil of the scalar x is the smallest integer i, such that i >= x

import numpy as np
d=[.5,1.5,10.1,3.4,5.6,2.3] ## Ceil will return the highest number 
print("Ceil :\n", np.ceil(d))

d1 = [133.000, 344.54, 437.56, 44.9, 1.2]
print("Ceil :\n", np.ceil(d1))
