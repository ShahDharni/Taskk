## Create an empty array :-

import numpy as np
empty_array=np.empty((3,4))
print(empty_array)


print()
## Create an Full numpy array
full_array=np.full((3,4),5)
print(full_array)


print()

empty_array1=np.empty([2,3])
print(empty_array1)


print()

full_array1=np.full([5,6],98)
